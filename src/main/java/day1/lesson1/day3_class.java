package day1.lesson1;

/**
 * 定义变量的格式:
 *    数据类型  变量名 = 初始化值
 *    数据类型 变量名;
 *    变量名 = 初始化值;
 *
 *
 */
public class day3_class {
    public static void main(String[] args){
        double d = 8.1;
        int num = (int) d;
        System.out.println(num);


        long b = 6000000000L;
        int c = (int) b;
        System.out.println(c);
    }
}
